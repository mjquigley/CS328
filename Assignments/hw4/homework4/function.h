///////////////////////////////////////////////////////////////////
/// @class Function
/// @brief This class outputs the result of the hardcoded function
///			1/(1+12x^2)
///////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////
/// @fn T operator()(T x)
/// @brief evaluate function at x
/// @pre the *, +, and / operators are defined for type T
/// @post returns the value of 1/(1+12x^2) at x
/// @return the value of 1/(1+12x^2) at x
///////////////////////////////////////////////////////////////////
#ifndef function_h
#define function_h

template <class T>
class Function{
public:
	T operator()(T x){
		return 1/(1+(12*(x*x)));
	}
};

#endif